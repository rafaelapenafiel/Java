public class Principal {
    
    public static void main(String[] args) {
       int s = 0;
       int contador = 0;
       for (int i = 0; i<=100; i++){
           if (i%2==1);
                s+= i;
                contador++;
    }
       double media = s / contador;
       System.out.println(media);
        
    }
}
